const norm = (s) => ' ' + String(s || '').toLowerCase().replace(/[^a-z0-9]+/g, ' ').trim() + ' ';

export function matchKB(text, max = 4){
  const hay = norm(text);
  const scored = [];
  for (const e of KB){
    let best = 0;
    for (const k of e.keys){
      const needle = norm(k).slice(1, -1);
      if (hay.includes(' ' + needle + ' ') || hay.includes(' ' + needle + 's ')){
        best = Math.max(best, k.split(' ').length * 100 + k.length);
      }
    }
    if (best) scored.push([best, e]);
  }
  scored.sort((a, b) => b[0] - a[0]);
  return scored.slice(0, max).map(s => s[1]);
}

export function kbPromptBlock(entries){
  if (!entries || !entries.length) return '';
  const blocks = entries.map(e => {
    const lines = [`■ ${e.name} — ${e.kind}`,
      `  silhouette: ${e.silhouette}`,
      `  build: ${e.build}`];
    if (e.pose) lines.push(`  pose: ${e.pose}`);
    if (e.animate) lines.push(`  animate: ${e.animate}`);
    if (e.palette) lines.push(`  palette: ${e.palette}`);
    if (e.flyAs) lines.push(`  fly as: ${e.flyAs}`);
    if (e.art) lines.push(`  reference wireframe (100x100 pad, y down) — start from EXACTLY this, then adapt pose/palette to the scenario like a keyframe:\n  ${JSON.stringify(e.art)}`);
    return lines.join('\n');
  });
  return `VISUAL REFERENCE (ground truth — these override your own guess of what the subject looks like):
${blocks.join('\n\n')}

Rules for using the reference:
- Entries with a wireframe: reproduce it as the group's "art" (shape:"custom"), adapting pose between acts like keyframes and recoloring only if the scenario demands.
- Entries without a wireframe: follow the build sheet part by part in "art" — every listed part is a separate fill with its own color.
- People are recognized by silhouette + pose + costume + palette. Never attempt facial detail beyond what the build sheet lists.
- "fly as" entries: skip custom art and use the stated shape/emoji instead.
- SHARPNESS: keep neighboring parts in clearly different colors and leave 2-4 sketchpad units of air between parts that must read separately (head vs shoulders, wing vs body) — overlapping same-color masses smear into blobs at 600 drones.`;
}
